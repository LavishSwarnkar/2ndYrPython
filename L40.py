list1 = [231, 34, 22, 42, 15, 65, 656, 765]
print("list1:", list1)

print("Max:", max(list1))
print("Min:", min(list1))
print("Sum:", sum(list1))

list1.sort()
print("Ascending Sort:", list1)

list1.reverse()
print("Descending Sort:", list1, "\n")


